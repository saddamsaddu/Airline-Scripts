CREATE TABLE AR_LOGIN

(

PK_ID INT IDENTITY(1,1) PRIMARY KEY,

USERNAME VARCHAR(100),

[PASSWORD] VARCHAR(500),

USERTYPE CHAR(1),

ISACTIVE CHAR(1),

LASTLOGINDATE DATE
)

 

 

CREATE TABLE AR_REGISTRATION

(

PK_ID INT IDENTITY(1,1),

HDR_FK INT FOREIGN KEY REFERENCES AR_LOGIN(PK_ID),

TITLE VARCHAR(10),

NAME VARCHAR(500),

GENDER VARCHAR(10),

EMAILID VARCHAR(500),

MOBILENO VARCHAR(10),

PASSPORTNO VARCHAR(20),

AADHARNO VARCHAR(20),

PANNO VARCHAR(20),

ADDRESS VARCHAR(MAX)

)

 

 

CREATE TABLE AR_GENERAL

(

PK_ID INT IDENTITY(1,1),

LISTGROUP VARCHAR(100),

LISTCODE VARCHAR(100),

LISTNAME VARCHAR(100),

ISACTIVE CHAR(1)

)

CREATE TABLE FLIGHTDET

(

PK_ID INT IDENTITY(1,1) PRIMARY KEY,

FLIGHT_ID VARCHAR(10),

FLIGHT_NAME VARCHAR(100),

FROM_PLACE VARCHAR(100),

TO_PLACE VARCHAR(100),


ARRIVAL_TIME DATETIME,

DEPATURE_TIME DATETIME,

SEATS VARCHAR(10),

TICKET_PRICE INT,

CRTDBY VARCHAR(20),

CRTDON DATE,

ISACTIVE BIT

)

